from __future__ import annotations

import argparse
import json
import re
from dataclasses import dataclass
from pathlib import Path
from typing import Any

import pandas as pd


RUN_BEGIN_RE = re.compile(r"^### RUN BEGIN(?: run_name=(?P<run_name>.+?))? ###$")
RUN_END_RE = re.compile(r"^### RUN END(?: run_name=(?P<run_name>.+?))? ###$")
PROFILE_RE = re.compile(
    r"^Engine::query profiler:(?P<indent>\s*)"
    r"(?P<segment>.+):\s*(?P<elapsed_ms>\d+(?:\.\d+)?)\s*ms$"
)
PARTITION_SEGMENT_RE = re.compile(r"^partition (?P<partition_id>\d+) (?P<stage>.+)$")

RUN_CONFIG_PREFIX = "GPJSON_RUN_CONFIG "


@dataclass(frozen=True)
class RunRecord:
    source_file: str
    run_index: int
    run_name: str
    config: dict[str, Any]


@dataclass(frozen=True)
class TimingRecord:
    source_file: str
    run_index: int
    run_name: str
    sequence_index: int
    partition_id: int | None
    indent_level: int
    segment: str
    stage: str
    elapsed_ms: float


def parse_json_payload(line: str, prefix: str) -> dict[str, Any]:
    return json.loads(line[len(prefix) :])


def fallback_run_name(source_file: str, run_index: int) -> str:
    return f"{Path(source_file).stem}_run_{run_index}"


def parse_log(path: Path) -> tuple[list[RunRecord], list[TimingRecord]]:
    runs: list[RunRecord] = []
    timings: list[TimingRecord] = []

    current_run_index = -1
    current_run_name: str | None = None
    current_config: dict[str, Any] | None = None
    current_partition_id: int | None = None

    with path.open("r", encoding="utf-8") as log_file:
        for line_number, raw_line in enumerate(log_file, start=1):
            line = raw_line.rstrip("\n")

            begin_match = RUN_BEGIN_RE.match(line)
            if begin_match:
                current_run_index += 1
                current_run_name = begin_match.group("run_name")
                current_config = None
                current_partition_id = None
                continue

            end_match = RUN_END_RE.match(line)
            if end_match:
                if current_config is None:
                    name = current_run_name or fallback_run_name(
                        str(path), current_run_index
                    )
                    current_config = {"run_name": name}
                    runs.append(
                        RunRecord(str(path), current_run_index, name, current_config)
                    )
                current_run_name = None
                current_config = None
                current_partition_id = None
                continue

            if line.startswith(RUN_CONFIG_PREFIX):
                config = parse_json_payload(line, RUN_CONFIG_PREFIX)
                name = str(
                    config.get("run_name")
                    or current_run_name
                    or fallback_run_name(str(path), current_run_index)
                )
                if current_run_index < 0:
                    current_run_index = 0
                current_run_name = name
                current_config = config
                runs.append(RunRecord(str(path), current_run_index, name, config))
                continue

            profile_match = PROFILE_RE.match(line)
            if profile_match:
                if current_run_index < 0:
                    current_run_index = 0
                name = current_run_name or fallback_run_name(
                    str(path), current_run_index
                )
                segment = profile_match.group("segment").strip()
                indent_level = len(profile_match.group("indent")) // 2
                elapsed_ms = float(profile_match.group("elapsed_ms"))

                partition_id = current_partition_id
                stage = segment
                partition_match = PARTITION_SEGMENT_RE.match(segment)
                if partition_match:
                    partition_id = int(partition_match.group("partition_id"))
                    current_partition_id = partition_id
                    stage = partition_match.group("stage")
                    if stage == "total":
                        current_partition_id = None

                timings.append(
                    TimingRecord(
                        source_file=str(path),
                        run_index=current_run_index,
                        run_name=name,
                        sequence_index=len(timings),
                        partition_id=partition_id,
                        indent_level=indent_level,
                        segment=segment,
                        stage=stage,
                        elapsed_ms=elapsed_ms,
                    )
                )
                continue

            if line.startswith("GPJSON_"):
                raise ValueError(f"{path}:{line_number}: unknown GPJSON record: {line}")

    return runs, timings


def records_to_dataframes(
    runs: list[RunRecord], timings: list[TimingRecord]
) -> tuple[pd.DataFrame, pd.DataFrame]:
    run_rows = []
    for run in runs:
        row = dict(run.config)
        row["source_file"] = run.source_file
        row["run_index"] = run.run_index
        row["run_name"] = run.run_name
        run_rows.append(row)

    runs_df = pd.json_normalize(run_rows, sep=".")
    timings_df = pd.DataFrame([record.__dict__ for record in timings])
    return runs_df, timings_df


def option_columns(runs_df: pd.DataFrame) -> list[str]:
    prefixes = ("engine_options.",)
    included = {"dataset_path", "queries"}
    return [
        column
        for column in runs_df.columns
        if column.startswith(prefixes) or column in included
    ]


def comparable_value(value: Any) -> Any:
    return (
        json.dumps(value, sort_keys=True) if isinstance(value, (dict, list)) else value
    )


def common_and_diff_options(
    runs_df: pd.DataFrame,
) -> tuple[pd.Series, pd.DataFrame]:
    if runs_df.empty:
        return pd.Series(dtype=object), pd.DataFrame()

    indexed = runs_df.set_index("run_name", drop=False)
    cols = option_columns(indexed)
    common: dict[str, Any] = {}
    diff_cols: list[str] = []

    for column in cols:
        values = indexed[column]
        comparable_values = values.map(comparable_value)
        if comparable_values.nunique(dropna=False) == 1:
            common[column] = values.iloc[0]
        else:
            diff_cols.append(column)

    diff_df = indexed[diff_cols] if diff_cols else pd.DataFrame(index=indexed.index)
    return pd.Series(common, name="value"), diff_df


def partition_timing_summary(timings_df: pd.DataFrame) -> pd.DataFrame:
    if timings_df.empty:
        return pd.DataFrame()

    partition_df = timings_df[timings_df["partition_id"].notna()].copy()
    if partition_df.empty:
        return pd.DataFrame()

    stages = [
        "load_to_device",
        "build_index",
        "execute_query",
        "materialize_results",
        "total",
    ]
    partition_df = partition_df[partition_df["stage"].isin(stages)]
    if partition_df.empty:
        return pd.DataFrame()

    summary = partition_df.pivot_table(
        index="run_name",
        columns="stage",
        values="elapsed_ms",
        aggfunc="sum",
    )
    present_stages = [stage for stage in stages if stage in summary.columns]
    summary = summary[present_stages].reset_index()
    return summary


def hierarchical_timing_summary(
    timings_df: pd.DataFrame, max_indent_level: int
) -> pd.DataFrame:
    if timings_df.empty:
        return pd.DataFrame()

    rows_by_path: dict[tuple[str, ...], dict[str, Any]] = {}
    path_order: list[tuple[str, ...]] = []
    run_names: list[str] = []
    sorted_timings = timings_df.sort_values(
        ["source_file", "run_index", "sequence_index"]
    )

    for _, group in sorted_timings.groupby(["source_file", "run_index"], sort=False):
        records = group.to_dict("records")
        run_name = str(records[0]["run_name"])
        if run_name not in run_names:
            run_names.append(run_name)

        children: dict[int, list[int]] = {index: [] for index in range(len(records))}
        roots: list[int] = []
        stack: dict[int, int] = {}

        for index in range(len(records) - 1, -1, -1):
            indent_level = int(records[index]["indent_level"])
            parent_index = stack.get(indent_level - 1)
            if parent_index is None:
                roots.append(index)
            else:
                children[parent_index].append(index)

            for stack_indent in list(stack):
                if stack_indent >= indent_level:
                    del stack[stack_indent]
            stack[indent_level] = index

        def emit(index: int, path: tuple[str, ...], depth: int) -> None:
            record = records[index]
            indent_level = int(record["indent_level"])
            segment = str(record["segment"])
            current_path = (*path, segment)

            if indent_level <= max_indent_level:
                if current_path not in rows_by_path:
                    path_order.append(current_path)
                    rows_by_path[current_path] = {
                        "hierarchy": hierarchy_label(segment, depth)
                    }
                row = rows_by_path[current_path]
                row[run_name] = row.get(run_name, 0.0) + record["elapsed_ms"]

            child_indices = sorted(
                children[index], key=lambda child: records[child]["sequence_index"]
            )
            for child_index in child_indices:
                emit(child_index, current_path, depth + 1)

        root_indices = sorted(roots, key=lambda root: records[root]["sequence_index"])
        for root_index in root_indices:
            emit(root_index, (), 0)

    rows = [rows_by_path[path] for path in path_order]
    if not rows:
        return pd.DataFrame(columns=["hierarchy", *run_names])

    summary = pd.DataFrame(rows)
    for run_name in run_names:
        if run_name not in summary.columns:
            summary[run_name] = pd.NA
    return summary[["hierarchy", *run_names]]


def hierarchy_label(segment: str, depth: int) -> str:
    if depth == 0:
        return segment

    return f"{'  ' * (depth - 1)}+-- {segment}"


def print_hierarchical_timing_summary(summary: pd.DataFrame) -> None:
    if summary.empty:
        print("No timing records.")
        return

    run_names = [column for column in summary.columns if column != "hierarchy"]
    formatted_rows: list[dict[str, str]] = []
    for _, row in summary.iterrows():
        formatted_row = {"hierarchy": str(row["hierarchy"])}
        for run_name in run_names:
            value = row[run_name]
            formatted_row[run_name] = "" if pd.isna(value) else f"{value:.3f}"
        formatted_rows.append(formatted_row)

    hierarchy_width = max(
        len("hierarchy"), *(len(row["hierarchy"]) for row in formatted_rows)
    )
    run_widths = {
        run_name: max(
            len(run_name), *(len(row[run_name]) for row in formatted_rows)
        )
        for run_name in run_names
    }

    header = "hierarchy".ljust(hierarchy_width)
    for run_name in run_names:
        header += f"  {run_name.rjust(run_widths[run_name])}"
    print(header)

    for row in formatted_rows:
        line = row["hierarchy"].ljust(hierarchy_width)
        for run_name in run_names:
            line += f"  {row[run_name].rjust(run_widths[run_name])}"
        print(line)


def print_section(title: str) -> None:
    print(f"\n{title}")
    print("=" * len(title))


def print_report(
    runs_df: pd.DataFrame, timings_df: pd.DataFrame, max_indent_level: int
) -> None:
    print_section("Runs")
    if runs_df.empty:
        print("No runs found.")
    else:
        runs_view = runs_df.copy()
        if "queries" in runs_view.columns:
            runs_view["query_count"] = runs_view["queries"].map(
                lambda value: len(value) if isinstance(value, list) else 0
            )
        else:
            runs_view["query_count"] = 0
        cols = ["run_name", "source_file", "dataset_path", "query_count"]
        print(
            runs_view[[col for col in cols if col in runs_view.columns]].to_string(
                index=False
            )
        )

    common, diff_df = common_and_diff_options(runs_df)

    print_section("Common Run Config")
    if common.empty:
        print("No common run config.")
    else:
        print(common.to_string())

    print_section("Differing Run Config")
    if diff_df.empty or len(diff_df.columns) == 0:
        print("No differing run config.")
    else:
        print(diff_df.to_string())

    print_section("Partition Timing Totals")
    partition_summary = partition_timing_summary(timings_df)
    if partition_summary.empty:
        print("No partition timing records.")
    else:
        print(partition_summary.to_string(index=False))

    print_section("Hierarchical Timings")
    hierarchical_summary = hierarchical_timing_summary(timings_df, max_indent_level)
    print_hierarchical_timing_summary(hierarchical_summary)


def non_negative_int(value: str) -> int:
    parsed_value = int(value)
    if parsed_value < 0:
        raise argparse.ArgumentTypeError("value must be non-negative")
    return parsed_value


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Parse gpjson profiler logs.")
    parser.add_argument(
        "--max-indent-level",
        type=non_negative_int,
        default=2,
        help="Maximum inclusive profiler indent level to print.",
    )
    parser.add_argument("logs", nargs="+", type=Path, help="Log files to parse.")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    runs: list[RunRecord] = []
    timings: list[TimingRecord] = []

    for log_path in args.logs:
        parsed_runs, parsed_timings = parse_log(log_path)
        runs.extend(parsed_runs)
        timings.extend(parsed_timings)

    runs_df, timings_df = records_to_dataframes(runs, timings)
    print_report(runs_df, timings_df, args.max_indent_level)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
