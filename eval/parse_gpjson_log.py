from __future__ import annotations

import argparse
import json
import re
from dataclasses import dataclass
from pathlib import Path
from typing import Any

import pandas as pd


RUN_BEGIN_RE = re.compile(r"^### RUN BEGIN(?: run_name=(?P<run_name>.+?))? ###$")
RUN_END_RE = re.compile(r"^### RUN END(?: run_name=(?P<run_name>.+?))? ###$")
PROFILE_RE = re.compile(
    r"^Engine::query profiler:(?P<indent>\s*)"
    r"(?P<segment>.+):\s*(?P<elapsed_ms>\d+(?:\.\d+)?)\s*ms$"
)
PARTITION_SEGMENT_RE = re.compile(r"^partition (?P<partition_id>\d+) (?P<stage>.+)$")

RUN_CONFIG_PREFIX = "GPJSON_RUN_CONFIG "


@dataclass(frozen=True)
class RunRecord:
    source_file: str
    run_index: int
    run_name: str
    config: dict[str, Any]


@dataclass(frozen=True)
class TimingRecord:
    source_file: str
    run_index: int
    run_name: str
    partition_id: int | None
    indent_level: int
    segment: str
    stage: str
    elapsed_ms: float


def parse_json_payload(line: str, prefix: str) -> dict[str, Any]:
    return json.loads(line[len(prefix) :])


def fallback_run_name(source_file: str, run_index: int) -> str:
    return f"{Path(source_file).stem}_run_{run_index}"


def parse_log(path: Path) -> tuple[list[RunRecord], list[TimingRecord]]:
    runs: list[RunRecord] = []
    timings: list[TimingRecord] = []

    current_run_index = -1
    current_run_name: str | None = None
    current_config: dict[str, Any] | None = None
    current_partition_id: int | None = None

    with path.open("r", encoding="utf-8") as log_file:
        for line_number, raw_line in enumerate(log_file, start=1):
            line = raw_line.rstrip("\n")

            begin_match = RUN_BEGIN_RE.match(line)
            if begin_match:
                current_run_index += 1
                current_run_name = begin_match.group("run_name")
                current_config = None
                current_partition_id = None
                continue

            end_match = RUN_END_RE.match(line)
            if end_match:
                if current_config is None:
                    name = current_run_name or fallback_run_name(
                        str(path), current_run_index
                    )
                    current_config = {"run_name": name}
                    runs.append(
                        RunRecord(str(path), current_run_index, name, current_config)
                    )
                current_run_name = None
                current_config = None
                current_partition_id = None
                continue

            if line.startswith(RUN_CONFIG_PREFIX):
                config = parse_json_payload(line, RUN_CONFIG_PREFIX)
                name = str(
                    config.get("run_name")
                    or current_run_name
                    or fallback_run_name(str(path), current_run_index)
                )
                if current_run_index < 0:
                    current_run_index = 0
                current_run_name = name
                current_config = config
                runs.append(RunRecord(str(path), current_run_index, name, config))
                continue

            profile_match = PROFILE_RE.match(line)
            if profile_match:
                if current_run_index < 0:
                    current_run_index = 0
                name = current_run_name or fallback_run_name(
                    str(path), current_run_index
                )
                segment = profile_match.group("segment").strip()
                indent_level = len(profile_match.group("indent")) // 2
                elapsed_ms = float(profile_match.group("elapsed_ms"))

                partition_id = current_partition_id
                stage = segment
                partition_match = PARTITION_SEGMENT_RE.match(segment)
                if partition_match:
                    partition_id = int(partition_match.group("partition_id"))
                    current_partition_id = partition_id
                    stage = partition_match.group("stage")
                    if stage == "total":
                        current_partition_id = None

                timings.append(
                    TimingRecord(
                        source_file=str(path),
                        run_index=current_run_index,
                        run_name=name,
                        partition_id=partition_id,
                        indent_level=indent_level,
                        segment=segment,
                        stage=stage,
                        elapsed_ms=elapsed_ms,
                    )
                )
                continue

            if line.startswith("GPJSON_"):
                raise ValueError(f"{path}:{line_number}: unknown GPJSON record: {line}")

    return runs, timings


def records_to_dataframes(
    runs: list[RunRecord], timings: list[TimingRecord]
) -> tuple[pd.DataFrame, pd.DataFrame]:
    run_rows = []
    for run in runs:
        row = dict(run.config)
        row["source_file"] = run.source_file
        row["run_index"] = run.run_index
        row["run_name"] = run.run_name
        run_rows.append(row)

    runs_df = pd.json_normalize(run_rows, sep=".")
    timings_df = pd.DataFrame([record.__dict__ for record in timings])
    return runs_df, timings_df


def option_columns(runs_df: pd.DataFrame) -> list[str]:
    prefixes = ("engine_options.",)
    included = {"dataset_path", "queries"}
    return [
        column
        for column in runs_df.columns
        if column.startswith(prefixes) or column in included
    ]


def comparable_value(value: Any) -> Any:
    return (
        json.dumps(value, sort_keys=True) if isinstance(value, (dict, list)) else value
    )


def common_and_diff_options(
    runs_df: pd.DataFrame,
) -> tuple[pd.Series, pd.DataFrame]:
    if runs_df.empty:
        return pd.Series(dtype=object), pd.DataFrame()

    indexed = runs_df.set_index("run_name", drop=False)
    cols = option_columns(indexed)
    common: dict[str, Any] = {}
    diff_cols: list[str] = []

    for column in cols:
        values = indexed[column]
        comparable_values = values.map(comparable_value)
        if comparable_values.nunique(dropna=False) == 1:
            common[column] = values.iloc[0]
        else:
            diff_cols.append(column)

    diff_df = indexed[diff_cols] if diff_cols else pd.DataFrame(index=indexed.index)
    return pd.Series(common, name="value"), diff_df


def partition_timing_summary(timings_df: pd.DataFrame) -> pd.DataFrame:
    if timings_df.empty:
        return pd.DataFrame()

    partition_df = timings_df[timings_df["partition_id"].notna()].copy()
    if partition_df.empty:
        return pd.DataFrame()

    stages = [
        "load_to_device",
        "build_index",
        "execute_query",
        "materialize_results",
        "total",
    ]
    partition_df = partition_df[partition_df["stage"].isin(stages)]
    if partition_df.empty:
        return pd.DataFrame()

    summary = partition_df.pivot_table(
        index="run_name",
        columns="stage",
        values="elapsed_ms",
        aggfunc="sum",
    )
    present_stages = [stage for stage in stages if stage in summary.columns]
    summary = summary[present_stages].reset_index()
    return summary


def print_section(title: str) -> None:
    print(f"\n{title}")
    print("=" * len(title))


def print_report(runs_df: pd.DataFrame, timings_df: pd.DataFrame) -> None:
    print_section("Runs")
    if runs_df.empty:
        print("No runs found.")
    else:
        runs_view = runs_df.copy()
        if "queries" in runs_view.columns:
            runs_view["query_count"] = runs_view["queries"].map(
                lambda value: len(value) if isinstance(value, list) else 0
            )
        else:
            runs_view["query_count"] = 0
        cols = ["run_name", "source_file", "dataset_path", "query_count"]
        print(
            runs_view[[col for col in cols if col in runs_view.columns]].to_string(
                index=False
            )
        )

    common, diff_df = common_and_diff_options(runs_df)

    print_section("Common Run Config")
    if common.empty:
        print("No common run config.")
    else:
        print(common.to_string())

    print_section("Differing Run Config")
    if diff_df.empty or len(diff_df.columns) == 0:
        print("No differing run config.")
    else:
        print(diff_df.to_string())

    print_section("Partition Timing Totals")
    partition_summary = partition_timing_summary(timings_df)
    if partition_summary.empty:
        print("No partition timing records.")
    else:
        print(partition_summary.to_string(index=False))


