from __future__ import annotations

import argparse
from pathlib import Path

from .dataframes import records_to_dataframes
from .models import RunRecord, TimingRecord
from .parser import parse_log
from .report import print_report


def non_negative_int(value: str) -> int:
    parsed_value = int(value)
    if parsed_value < 0:
        raise argparse.ArgumentTypeError("value must be non-negative")
    return parsed_value


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Parse gpjson profiler logs.")
    parser.add_argument(
        "--max-indent-level",
        type=non_negative_int,
        default=2,
        help="Maximum inclusive profiler indent level to print.",
    )
    parser.add_argument(
        "--sum-partitions",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="Sum matching hierarchical timing segments across partitions.",
    )
    parser.add_argument(
        "--baseline-run",
        help="Run name to use as the baseline for relative timing annotations.",
    )
    parser.add_argument(
        "--relative-to-baseline",
        choices=("none", "speedup", "reduction"),
        default="none",
        help=(
            "Annotate hierarchical timings relative to the baseline as speedup "
            "or percent time reduction."
        ),
    )
    parser.add_argument("logs", nargs="+", type=Path, help="Log files to parse.")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    runs: list[RunRecord] = []
    timings: list[TimingRecord] = []

