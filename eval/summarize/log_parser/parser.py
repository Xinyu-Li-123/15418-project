from __future__ import annotations

import json
import re
from pathlib import Path
from typing import Any

from .models import RunRecord, TimingRecord


RUN_BEGIN_RE = re.compile(r"^### RUN BEGIN(?: run_name=(?P<run_name>.+?))? ###$")
RUN_END_RE = re.compile(r"^### RUN END(?: run_name=(?P<run_name>.+?))? ###$")
PROFILE_RE = re.compile(
    r"^Engine::query profiler:(?P<indent>\s*)"
    r"(?P<segment>.+):\s*(?P<elapsed_ms>\d+(?:\.\d+)?)\s*ms$"
)
PARTITION_SEGMENT_RE = re.compile(r"^partition (?P<partition_id>\d+) (?P<stage>.+)$")

RUN_CONFIG_PREFIX = "GPJSON_RUN_CONFIG "


def parse_json_payload(line: str, prefix: str) -> dict[str, Any]:
    return json.loads(line[len(prefix) :])


def fallback_run_name(source_file: str, run_index: int) -> str:
    return f"{Path(source_file).stem}_run_{run_index}"


def parse_log(path: Path) -> tuple[list[RunRecord], list[TimingRecord]]:
    runs: list[RunRecord] = []
    timings: list[TimingRecord] = []

    current_run_index = -1
    current_run_name: str | None = None
    current_config: dict[str, Any] | None = None
    current_partition_id: int | None = None

    with path.open("r", encoding="utf-8") as log_file:
        for line_number, raw_line in enumerate(log_file, start=1):
            line = raw_line.rstrip("\n")

            begin_match = RUN_BEGIN_RE.match(line)
            if begin_match:
                current_run_index += 1
                current_run_name = begin_match.group("run_name")
                current_config = None
                current_partition_id = None
                continue

            end_match = RUN_END_RE.match(line)
            if end_match:
                if current_config is None:
                    name = current_run_name or fallback_run_name(
                        str(path), current_run_index
                    )
                    current_config = {"run_name": name}
                    runs.append(
                        RunRecord(str(path), current_run_index, name, current_config)
                    )
                current_run_name = None
                current_config = None
                current_partition_id = None
                continue

            if line.startswith(RUN_CONFIG_PREFIX):
                config = parse_json_payload(line, RUN_CONFIG_PREFIX)
                name = str(
                    config.get("run_name")
                    or current_run_name
                    or fallback_run_name(str(path), current_run_index)
                )
                if current_run_index < 0:
                    current_run_index = 0
                current_run_name = name
                current_config = config
                runs.append(RunRecord(str(path), current_run_index, name, config))
                continue

            profile_match = PROFILE_RE.match(line)
            if profile_match:
                if current_run_index < 0:
                    current_run_index = 0
                name = current_run_name or fallback_run_name(
                    str(path), current_run_index
                )
                segment = profile_match.group("segment").strip()
                indent_level = len(profile_match.group("indent")) // 2
                elapsed_ms = float(profile_match.group("elapsed_ms"))

                partition_id = current_partition_id
                stage = segment
                partition_match = PARTITION_SEGMENT_RE.match(segment)
                if partition_match:
                    partition_id = int(partition_match.group("partition_id"))
                    current_partition_id = partition_id
                    stage = partition_match.group("stage")
                    if stage == "total":
                        current_partition_id = None

                timings.append(
                    TimingRecord(
                        source_file=str(path),
                        run_index=current_run_index,
                        run_name=name,
                        sequence_index=len(timings),
                        partition_id=partition_id,
                        indent_level=indent_level,
                        segment=segment,
                        stage=stage,
                        elapsed_ms=elapsed_ms,
                    )
                )
                continue

            if line.startswith("GPJSON_"):
                raise ValueError(f"{path}:{line_number}: unknown GPJSON record: {line}")

    return runs, timings
