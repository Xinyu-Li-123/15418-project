def option_columns(runs_df: pd.DataFrame) -> list[str]:
    prefixes = ("engine_options.",)
    included = {"dataset_path", "queries"}
    return [
        column
        for column in runs_df.columns
        if column.startswith(prefixes) or column in included
    ]


def comparable_value(value: Any) -> Any:
    return (
        json.dumps(value, sort_keys=True) if isinstance(value, (dict, list)) else value
    )


def common_and_diff_options(
    runs_df: pd.DataFrame,
) -> tuple[pd.Series, pd.DataFrame]:
    if runs_df.empty:
        return pd.Series(dtype=object), pd.DataFrame()

    indexed = runs_df.set_index("run_name", drop=False)
    cols = option_columns(indexed)
    common: dict[str, Any] = {}
    diff_cols: list[str] = []

    for column in cols:
        values = indexed[column]
        comparable_values = values.map(comparable_value)
        if comparable_values.nunique(dropna=False) == 1:
            common[column] = values.iloc[0]
        else:
            diff_cols.append(column)

    diff_df = indexed[diff_cols] if diff_cols else pd.DataFrame(index=indexed.index)
    return pd.Series(common, name="value"), diff_df


def partition_timing_summary(timings_df: pd.DataFrame) -> pd.DataFrame:
    if timings_df.empty:
        return pd.DataFrame()

    partition_df = timings_df[timings_df["partition_id"].notna()].copy()
    if partition_df.empty:
        return pd.DataFrame()

    stages = [
        "load_to_device",
        "build_index",
        "execute_query",
        "materialize_results",
        "total",
    ]
    partition_df = partition_df[partition_df["stage"].isin(stages)]
    if partition_df.empty:
        return pd.DataFrame()

    summary = partition_df.pivot_table(
        index="run_name",
        columns="stage",
        values="elapsed_ms",
        aggfunc="sum",
    )
    present_stages = [stage for stage in stages if stage in summary.columns]
    summary = summary[present_stages].reset_index()
    return summary


def hierarchical_timing_summary(
    timings_df: pd.DataFrame, max_indent_level: int, sum_partitions: bool
) -> pd.DataFrame:
    if timings_df.empty:
        return pd.DataFrame()

    rows_by_path: dict[tuple[str, ...], dict[str, Any]] = {}
    path_order: list[tuple[str, ...]] = []
    run_names: list[str] = []
    sorted_timings = timings_df.sort_values(
        ["source_file", "run_index", "sequence_index"]
    )

    for _, group in sorted_timings.groupby(["source_file", "run_index"], sort=False):
        records = group.to_dict("records")
        run_name = str(records[0]["run_name"])
        if run_name not in run_names:
            run_names.append(run_name)

        children: dict[int, list[int]] = {index: [] for index in range(len(records))}
        roots: list[int] = []
        stack: dict[int, int] = {}

        for index in range(len(records) - 1, -1, -1):
            indent_level = int(records[index]["indent_level"])
            parent_index = stack.get(indent_level - 1)
            if parent_index is None:
                roots.append(index)
            else:
                children[parent_index].append(index)

            for stack_indent in list(stack):
                if stack_indent >= indent_level:
                    del stack[stack_indent]
            stack[indent_level] = index

        def emit(index: int, path: tuple[str, ...], depth: int) -> None:
            record = records[index]
            indent_level = int(record["indent_level"])
            segment = hierarchy_segment(record, sum_partitions)
            current_path = (*path, segment)

            if indent_level <= max_indent_level:
                if current_path not in rows_by_path:
                    path_order.append(current_path)
                    rows_by_path[current_path] = {
                        "hierarchy": hierarchy_label(segment, depth)
                    }
                row = rows_by_path[current_path]
                row[run_name] = row.get(run_name, 0.0) + record["elapsed_ms"]

            child_indices = sorted(
                children[index], key=lambda child: records[child]["sequence_index"]
            )
            for child_index in child_indices:
                emit(child_index, current_path, depth + 1)

        root_indices = sorted(roots, key=lambda root: records[root]["sequence_index"])
        for root_index in root_indices:
            emit(root_index, (), 0)

    rows = [rows_by_path[path] for path in path_order]
    if not rows:
        return pd.DataFrame(columns=["hierarchy", *run_names])

    summary = pd.DataFrame(rows)
    for run_name in run_names:
        if run_name not in summary.columns:
            summary[run_name] = pd.NA
    return summary[["hierarchy", *run_names]]


def hierarchy_segment(record: dict[str, Any], sum_partitions: bool) -> str:
    segment = str(record["segment"])
    if not sum_partitions or pd.isna(record["partition_id"]):
        return segment

    partition_match = PARTITION_SEGMENT_RE.match(segment)
    if partition_match is None:
        return segment

    return f"partitions {partition_match.group('stage')}"
