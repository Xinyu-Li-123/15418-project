from __future__ import annotations

import pandas as pd

from .models import RunRecord, TimingRecord


def records_to_dataframes(
    runs: list[RunRecord], timings: list[TimingRecord]
) -> tuple[pd.DataFrame, pd.DataFrame]:
    run_rows = []
    for run in runs:
        row = dict(run.config)
        row["source_file"] = run.source_file
        row["run_index"] = run.run_index
        row["run_name"] = run.run_name
        run_rows.append(row)

    runs_df = pd.json_normalize(run_rows, sep=".")
    timings_df = pd.DataFrame([record.__dict__ for record in timings])
    return runs_df, timings_df
